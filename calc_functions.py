# add(a, b): Menerima dua argumen bilangan bulat dan mengembalikan hasil penjumlahan mereka
def add(a,b):
    return a+b

# subtract(a, b): Menerima dua argumen bilangan bulat dan mengembalikan hasil pengurangan a dikurangkan dari b.
def subtract(a,b):
    return a-b

# multiply(a, b): Menerima dua argumen bilangan bulat dan mengembalikan hasil perkalian a dikalikan dengan b.
def multiply(a,b):
    return a*b

# div(a,b): Menerima dua argumen bilangan bulat dan mengembalikan hasil pembagian bulan a dibagi dengan b.
def div(a,b):
    return a/b
